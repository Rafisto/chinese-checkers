func (b *ClassicBoard) PrintBoard() {
	for i := 0; i < 17; i++ {
		for j := 0; j < 25; j++ {
			if b.board[i][j] == -1 {
				print("- ")
			} else {
				print(b.board[i][j])
				print(" ")
			}
		}
		print("\n")
	}
}
